
public class Datum {
	private int den;
	private int mesic;
	private int rok;
	
	public Datum(int d, int m, int r) {
		this.den = d;
		this.mesic = m;
		this.rok =r;
	}
	
	
	
	
	public int getDen() {
		return den;
	}
	public void setDen(int den) {
		this.den = den;
	}
	public int getMesic() {
		return mesic;
	}
	public void setMesic(int mesic) {
		this.mesic = mesic;
	}
	public int getRok() {
		return rok;
	}
	public void setRok(int rok) {
		this.rok = rok;
	}
	
	
	
}
